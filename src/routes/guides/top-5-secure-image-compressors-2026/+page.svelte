        <section id="jpeg-encoder" class="my-16">
            <h2 class="text-3xl font-black text-[#4A2C2C] mb-6">The JPEG Encoder Question</h2>
            <p class="mb-6">
                Here's an uncomfortable truth: most web users will still need JPEG output for years to come. Marketplaces like eBay and Etsy, legacy CMSs, and client contracts often specify JPEG-only delivery. So when modern formats aren't an option, <em>which JPEG encoder</em> your tool uses becomes critical.
            </p>

            <div class="space-y-8">
                <div>
                    <h3 class="text-xl font-bold text-[#4A2C2C] mb-3">JPEG Encoders Are Not Equal</h3>
                    <p class="mb-4">All JPEG encoders produce files that browsers understand - this is the "interoperable" part. But the <em>efficiency</em> of that encoding - how many bytes you need to achieve a given visual quality - varies widely.</p>

                    <ul class="space-y-4 mb-6">
                        <li class="pl-6 border-l-4 border-gray-200 py-2">
                            <strong>Baseline libjpeg</strong> (1991): The original standard. Works everywhere, but wastes bytes by modern standards.
                        </li>
                        <li class="pl-6 border-l-4 border-blue-200 py-2">
                            <strong>MozJPEG</strong> (2014): Mozilla's optimizer. Improved quantization and trellis encoding deliver ~5-10% smaller files at equivalent quality. Battle-tested and widely deployed (used by Squoosh).
                        </li>
                        <li class="pl-6 border-l-4 border-[#F06292] py-2 bg-pink-50/30">
                            <strong>jpegli</strong> (2024): Google's latest encoder, designed for high-quality compression. In Google's tests, jpegli delivers <strong>up to 35% better compression at Q90-95</strong> (the "visually lossless" range photographers care about). It's API/ABI compatible with libjpeg-turbo and MozJPEG, making adoption friction-low for developers.
                        </li>
                    </ul>
                </div>

                <div>
                    <h3 class="text-xl font-bold text-[#4A2C2C] mb-3">Why This Matters for Mochify Users</h3>
                    <p class="mb-4">When you're uploading 500 product photos to eBay or compressing images for a client who mandates JPEG, jpegli means <em>smaller files at the same quality</em> - or the same file size at higher quality. For a 5MB DSLR photo, that's the difference between a 400KB output and a 280KB output, both looking identical to the customer.</p>
                    <p class="mb-4">Most tools (TinyPNG, ShortPixel) use proprietary or older JPEG stacks. Squoosh transparently uses MozJPEG. Mochify is the only mainstream web tool explicitly using jpegli for standard JPEG output.</p>
                    
                    <InfoBox type="tip">
                        <strong>Key Point:</strong> jpegli is <em>not</em> a new format. It produces standard <code class="bg-pink-50 text-pink-600 px-1.5 py-0.5 rounded text-sm font-bold border border-pink-100">.jpg</code> files that open in any viewer, browser, or app. The encoder efficiency is invisible to end-users - they just see faster-loading images.
                    </InfoBox>
                </div>
            </div>
        </section>

