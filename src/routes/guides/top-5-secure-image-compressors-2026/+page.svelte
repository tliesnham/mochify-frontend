            <h2 class="text-3xl font-black text-[#4A2C2C] mb-6">Why Modern Format Support Matters</h2>
            <p class="mb-6">In 2026, delivering only JPEG and PNG means leaving performance (and money) on the table.</p>

            <div class="space-y-8">
                <div>
                    <h3 class="text-xl font-bold text-[#4A2C2C] mb-3">HEIC: The iPhone Photographer's Pain Point</h3>
                    <p class="mb-4">Every iPhone since 2017 defaults to HEIC (High Efficiency Image Container). These files are 40–50% smaller than equivalent JPEGs at the same quality, but they're rejected by most marketplaces, CMSs, and email platforms. Tools that don't handle HEIC force users into a two-step workflow: convert elsewhere, then compress. Mochify and ShortPixel are the only mainstream compressors that accept HEIC natively.</p>
                </div>

                <div>
                    <h3 class="text-xl font-bold text-[#4A2C2C] mb-3">AVIF: The Web Performance Winner</h3>
                    <p class="mb-4">AVIF delivers ~20% smaller files than WebP at equivalent visual quality, with 93% browser support (Chrome, Firefox, Safari, Edge). For e-commerce, shaving 100KB off a product page hero image directly improves Largest Contentful Paint (LCP) scores - which Google uses as a Core Web Vitals ranking signal. Yet many "free" tools gate AVIF behind paid tiers. Mochify offers it free, as does Squoosh.</p>
                </div>

                <div>
                    <h3 class="text-xl font-bold text-[#4A2C2C] mb-3">JPEG XL: Future-Friendly Archives</h3>
                    <p class="mb-4">JPEG XL is technically superior to both AVIF and WebP: better compression, lossless JPEG transcoding, and 10-bit HDR support. Safari 17+ ships it by default, but Chrome and Firefox still require flags. For photographers and archivists, JPEG XL is the long-term bet. Only Squoosh, Cloudinary, and Mochify support it in 2026.</p>
                </div>

                <div class="bg-[#FFF5F7] rounded-3xl p-6 border border-pink-100">
                    <h4 class="text-lg font-black text-[#875F42] mb-3">The Practical Impact:</h4>
                    <p class="text-[#6C3F31]">Tools that lack these formats put users on the back foot. You either accept inferior compression (JPEG/PNG only) or juggle multiple tools to convert, compress, and convert again.</p>
                </div>
